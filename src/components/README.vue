<h3 id="mengenai-ssh">Mengenai SSH</h3>
<p>Secure Shell (SSH), adalah salah satu protokol jaringan dengan nomor port 22. SSH digunakan untuk melakukan komunkiasi dengan server dengan aman, SSH akan melakukan enkripsi kepada data yang terkirim, sehingga data tersebut tidak dapat mudah dibaca.</p>
<p>SSH bekerja dengan client-server. Dimana client menghubungi server SSH untuk melakukan pekerjaan disana. Pekerjaan bisa bermacam-macam, control kepada terminal, file copy, dan lain-lain. </p>
