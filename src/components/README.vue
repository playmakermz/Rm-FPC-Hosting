<h1 id="mengenai-website">Mengenai website</h1>
<p>sebuah website ditulis dengan aturan html atau xhtml, terkadang berisi dengan bahasa script untuk membantu website lebih hidup.</p>
<p>Beberapa protocol dalam website:</p>
<ul>
<li>HTTP (Hypertext Transfer Protocol)
HTTP adalah suatu protocol permintaan-respons, dimana client akan melakukan perminataan kepada server dan akan dibalas dengan informasi atau data yang tersedia dalam server.</li>
<li>HTTPS (Hypertext Transfer Protocol Secure)
HTTPS memiliki cara kerja yang hampir sama dengan HTTP, perbedaan adalah disana terdapat enkripsi TLS ( Transport Layer Security )tambah untuk meningkatkan keamanan jaringan. Penambahan kemanan salah satunnya berada di sisi server.</li>
</ul>
<p>Mengenai website dinamis dan statis:</p>
<ul>
<li>website statis
adalah sekumpulan dokumen atau halaman yang hanya bisa dirubah secara manual. didalam web statis tidak tersedia database dan web server framework ( contoh: django, nodejs ). biasanya terdiri dari html, css dan javascript ( javascript hanya untuk tampilan saja ).</li>
<li>website dinamis
adalah sekumpulan dokumen atau halaman yang bisa dengan mudah berubah dan bisa beroperasi sesuai dengan keinginan sang pembuat website. didalam sana ada local storage atau database untuk menyimpan data. dibangun dengan sistem yang complex ( contoh website dinamis: instagram, facebook )</li>
</ul>
<h2 id="mengenal-web-developer">Mengenal Web developer</h2>
